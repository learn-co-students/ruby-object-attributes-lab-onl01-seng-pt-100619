class Dog 
  def name(dog_name)
    @name = dog_name 
  end 
  
  def name 
    @name 
  end 
  
  def name=(new_dog_name) 
    @name = new_dog_name 
  end 
  
  def breed(dog_breed) 
    @breed = dog_breed 
  end  
  
  def breed 
    @breed 
  end 
  
  def breed=(new_breed) 
    @breed = new_breed 
  end 
end 

